/**
 * @param {character[][]} board
 * @param {string} word
 * @return {boolean}
 */
var exist = function(board, word) {
  /**
   * solution1: not effective if board is large
   */
  if (!Array.isArray(board)) return
  console.log(Date.now());

  const path = word.split('')
  const yLen = board.length
  const xLen = board[0].length
  let isExist = false

  for (let iy = 0; iy < yLen; iy++) {
    for (let ix = 0; ix < xLen; ix++) {
      if (board[iy][ix] === path[0]) {
        console.log('starting point: ', iy, ix);
        isExist = searchAdjacent(iy, ix, 0)
        console.log(isExist);
        if (isExist) return isExist
      }
    }
  }

  function searchAdjacent(iy, ix, chIdx) {
    if (
      iy < 0 || ix < 0 ||
      iy > yLen - 1 || ix > xLen - 1 ||
      board[iy][ix] !== path[chIdx]
    ) {
      return false
    }

    if (chIdx === path.length - 1) {
      // console.log('final chIdx: ', chIdx);
      return true
    }

    const temp = board[iy][ix]
    board[iy][ix] = 0
    const res = searchAdjacent(iy - 1, ix, chIdx + 1) ||
                searchAdjacent(iy + 1, ix, chIdx + 1) ||
                searchAdjacent(iy, ix - 1, chIdx + 1) ||
                searchAdjacent(iy, ix + 1, chIdx + 1)
    board[iy][ix] = temp

    return res
  }

  console.log(Date.now());

  return isExist
};

